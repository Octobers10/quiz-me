CREATE TABLE CATEGORY(
    CATEGORY_ID BIGINT IDENTITY(0,1) PRIMARY KEY,
    CATEGORY_NAME VARCHAR(16) NOT NULL
);

declare @ID int;

set @ID = 0;
select @MAX_ID = MAX(CATEGORY_ID FROM CATEGORY);

while @ID <= @MAX_ID AND @MAX_ID IS NOT NULL
    begin
        SELECT CATEGORY_NAME
        FROM CATEGORY
        WHERE ID = CATEGORY_ID
        CREATE TABLE CATEGORY_NAME(
            QUIZ_ID BIGINT IDENTITY(0,1) PRIMARY KEY,
            QUIZ_QUESTION VARCHAR NOT NULL, 
            QUIZ_ANSWER1 VARCHAR NOT NULL, 
            QUIZ_ANSWER2 VARCHAR NOT NULL, 
            QUIZ_ANSWER3 VARCHAR NOT NULL, 
            QUIZ_ANSWER4 VARCHAR NOT NULL
        )
        SET @ID = @ID + 1

    end